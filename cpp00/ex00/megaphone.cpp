#include <iostream>

int main(int argc, char **argv)
{
	if (argc == 1)
	{
		std::cout << "No words were spoken" << std::endl;
		return 0;
	}
	for(int x = 1; argv[x]; x++)
		for(int y = 0; argv[x][y]; y++)
			std::cout << (char)std::toupper(argv[x][y]);
	std::cout << std::endl;
	return 0;
}